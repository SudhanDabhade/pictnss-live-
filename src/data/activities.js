// Helper function to get activities by year
export const getActivitiesByYear = (year) => {
  return activities.filter(activity => activity.year === parseInt(year));
};

// Helper function to get activity by ID
export const getActivityById = (id) => {
  return activities.find(activity => activity.id === parseInt(id));
};

// Get all available years
export const getActivityYears = () => {
  return [...new Set(activities.map(a => a.year))].sort((a, b) => b - a);
};

